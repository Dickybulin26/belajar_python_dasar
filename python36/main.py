# list -> array, mengakses data dengan menggunakan index

data_list = ['udin','jamal','malik']

print(data_list[0])

# dictionary (dict) -> associative array
# identifier -> key

data_dict = {
    'key': 'value',
    'ud': 'udin',
    'ja': 'jamal',
    'ma': 'malik',
    'nmbr': 100,
    'list': data_list
}

print(data_dict['ud'])
print(data_dict['nmbr'])
print(data_dict['list'])